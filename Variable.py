# String
name = "pooja"
city = 'Delhi'

# Integer
age = 21
year = 2025

# Float
height = 5.4
price = 99.99

# Boolean
is_student = True
is_married = False

# NoneType
future_plan = None

# List (ordered, changeable, allows duplicates)
hobbies = ["reading", "coding", "traveling"]

# Tuple (ordered, unchangeable, allows duplicates)
grades = (90, 85, 78)

# Set (unordered, no duplicates)
visited_places = {"Goa", "Mumbai", "Delhi"}

# Dictionary (key-value pairs)
student_info = {
    "name": name,
    "age": age,
    "course": "Python"
}

# Constant (by naming convention)
PI = 3.14159

# Underscore variable (used for ignored or internal use)
_temp = "just temporary"

# Printing all variables
print("---- Basic Types ----")
print("Name:", name)
print("City:", city)
print("Age:", age)
print("Year:", year)
print("Height:", height)
print("Price:", price)
print("Is Student:", is_student)
print("Is Married:", is_married)
print("Future Plan:", future_plan)

print("\n---- Collection Types ----")
print("Hobbies (List):", hobbies)
print("Grades (Tuple):", grades)
print("Visited Places (Set):", visited_places)
print("Student Info (Dict):", student_info)

print("\n---- Others ----")
print("PI Constant:", PI)
print("Temporary Variable (_temp):", _temp)
