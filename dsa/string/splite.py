# The split() function is used to break a string into a list of
# words or parts based on a separator (by default, space " ").

# text = "I love Python programming"
# words = text.split()    # default separator is space
# print(words)
# # output
# ['I', 'love', 'Python', 'programming']

text = "I love Python programming"
words = text.split()    # default separator is space
print(words)

texts = "programming"
